// Mutable array that includes official + homebrew feats
export let FEATS: FeatData[] = [...OFFICIAL_FEATS];

/** Register homebrew feats into the global feats array */
export function registerHomebrewFeats(feats: FeatData[]): void {
  FEATS = [...OFFICIAL_FEATS, ...feats];
}

/** Remove all homebrew feats, restoring to official-only */
export function unregisterHomebrewFeats(): void {
  FEATS = [...OFFICIAL_FEATS];
}

export function getFeatById(id: string): FeatData | undefined {
  return FEATS.find((f) => f.id === id);
}

/** Returns true if a feat can be taken multiple times (has "more than once" in special or benefit text). */
export function isFeatRepeatable(feat: FeatData): boolean {
  const text = `${feat.special ?? ''} ${feat.benefit ?? ''}`.toLowerCase();
  return text.includes('more than once');
}

export function getMulticlassId(featIds: string[]): string | undefined {
  for (const id of featIds) {
    const feat = getFeatById(id);
    if (feat?.multiclassFor) return feat.multiclassFor;
  }
  return undefined;
}

export function featMeetsPrerequisites(
  feat: FeatData,
  raceId: string,
  classId: string,
  trainedSkills: string[],
  selectedFeatIds: string[],
  level: number,
  abilityScores?: Record<string, number>,
  deity?: string,
): boolean {
  const p = feat.prerequisites;
  if (p.minLevel && level < p.minLevel) return false;
  if (p.race?.length && !p.race.includes(raceId)) return false;
  if (p.class?.length && !p.class.includes(classId)) return false;
  if (p.abilities && abilityScores) {
    for (const [ab, min] of Object.entries(p.abilities)) {
      if ((abilityScores[ab] ?? 0) < min) return false;
    }
  }
  if (p.trainedSkill?.length) {
    if (!p.trainedSkill.some((s) => trainedSkills.includes(s))) return false;
  }
  if (p.anyMulticlassFeat) {
    const hasMC = selectedFeatIds.some((id) => getFeatById(id)?.multiclassFor);
    if (!hasMC) return false;
  }
  if (p.otherFeat && !selectedFeatIds.includes(p.otherFeat)) return false;
  if (p.deity && p.deity !== deity) return false;
  return true;
}
